    <h3><b>5 Tips para no gastar de más</b></h3>
    <br>
    
    <p><b>1) REVISA LOS GASTOS</b></p>
    <p>Anota todos tus gastos, incluso los más pequeños y detecta fugas de dinero. 
    También ve la posibilidad de conseguir una mejor negociación en gastos recurrentes.</p>
    <br>

    <p><b>2) CUIDADO CON LA EXTENSIÓN DE GARANTÍA</b></p>
    <p>Recuerda que por ley todos los productos electrónicos deben tener dos años de garantía. 
    Por lo tanto, a veces no es necesario comprar una extensión (por más que el vendedor te la 
    quiera vender a toda costa).</p>
    <br>

    <p><b>3) APROVECHA OFERTAS PARA TU OCIO</b></p>
    <p>Ejemplo: descuentos en restaurantes, viajes fuera de temporada, etc.</p>
    <br>

    <p><b>4) AHORRA MÁS</b></p>
    <p>Evita tener demasiado dinero a tu alcance, ese dinero que uno deja en la billetera "por las dudas".</p>
    <br>

    <p><b>5) FÍJATE QUÉ TAREAS PUEDES REALIZAR TU MISMO.</b></p>
    <p>Así ahorrarás la contratación. Ejemplo: reparar electrodomésticos, mantenimiento de la vivienda, etc.</p>
	<br>

	<h3><b>5 Trucos para ahorrar a la hora de comprar alimentos</b></h3>
	<br>
	<p>La alimentación es un aspecto fundamental también en las finanzas personales, 
	ya que en esto se gasta buena parte del presupuesto. Aquí unos tips para ahorrar:</p>
	<br>

	<p><b>1) Cíñete al presupuesto</b></p>
	<p>A la hora de ir a comprar lleva una lista y solo compra lo que está en ella. 
	No vayas a "mirar", eso hace que uno gaste de más.</p>
	<br>

	<p><b>2) Cocina tú mismo</b></p>
	<p>Esto es mucho más económico que comprar comida hecha. Algunas personas ven práctico dedicar un 
	día a la semana a cocinar y congelar los alimentos, entonces durante la semana al volver del trabajo 
	solo tienen que poner la comida en el microondas ¡y listo!</p>
	<br>

	<p><b>3) No lleves niños</b></p>
	<p>Según estudios, las personas que van al súper acompañadas de niños gastan varias veces más, ya que éstos piden cosas.</p>
	<br>

	<p><b>4) Aprovecha los productos propios del negocio</b></p>
	<p>Muchos hipermercados tienen productos propios a muy buen precio. Esto se debe a que no 
	tienen gastos de transporte ya que los fabrican ellos mismos.</p>
	<br>

	<p><b>5) Ve a tiendas especializadas</b></p>
	<p>Siempre va a ser más barato comprar en grandes supermercados antes que en pequeños almacenes de barrio.</p>
	<br>

	<p><b>6) Aprovecha las promos de tu banco</b></p>
	<p>Muchos bancos ofrecen días de promos exclusivas en determinadas sucursales. 
	Te recomendamos que antes de ir, verifiques qué locales están adheridos a estas promos y/o descuentos.</p>
