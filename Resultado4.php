	<h5 class="subtitulos"> 5) TOMA ACCIÓN. </h5>
	<p> No te quedes solo con la ilusión. Esfuérzate por lograrlo. </p>
	<IMG SRC="images/rata.jpg" alt="saliendo de la carrera de la rata" width="300px" height="300px" ALIGN=RIGHT>
	<p> Una buena meta es proponerte para tu próxima compra ahorrar primero, y comprar después. Esto es clave para salir de la carrera de la rata, porque al hacerlo logramos ir por delante del dinero no por detrás. Además nos permite evitar la trampa de comprar por impulso, que es lo que nos lleva a adquirir artículos innecesarios. Y por si fuera poco nos ahorramos enormes cantidades de dinero en intereses, ya que no necesitaremos crédito o financiamiento para obtener lo que queremos. </p>
	<p> Entonces traza un plan de acción. Por ejemplo, supongamos que quieres irte de vacaciones con tu familia el próximo año. Calcula cuánto dinero necesitarás y luego divídelo por los meses que tienes por delante hasta esa fecha. El resultado es lo que debes ahorrar mensualmente para ese fin. Lo mismo aplica a compras de electrodomésticos, tecnología o vehículos. </p>
	<p> Otra forma de tomar acción es amortizar tus compras. Esto es un poquito más complejo, especialmente en los países donde hay inflación. </p>
	<p> AMORTIZAR significa dividir el dinero que pagaste en un bien, por la vida útil del mismo. Ese dinero lo debes ahorrar. Entonces cuando el producto adquirido ya no funcione, podrás comprar uno nuevo sin necesidad de recurrir a créditos o tarjetas. </p>
	<p> Ejemplo: compras una heladera de 1.000 dólares que tiene una vida util de 10 años. 1000 dividido 10 da como resultado 100. Debes ahorrar 100 dólares por año durante 10 años. Entonces podrás tener una heladera nueva cuando lo necesites. </p>
	<h5> ¿Cómo hacerlo si en tu país existe inflación? </h5>
	<p> Como es bastante impredecible lo mejor es calcular anualmente. Es decir tomar la inflación del último año de tu país. Usa el dato de la fuente que consideres más confiable. Entonces se lo sumamos a la amortización. </p>
	<p> Veamos esto con el ejemplo de la heladera. Supongamos que en el último año la inflación en mi país fue del 47%. Entonces este año debería ahorrar 147 dólares para mi nueva heladera. El próximo año deberé hacer lo mismo. Ir actualizando mis cálculos con la inflación. </p>
	<p> Obviamente esto va ser un cálculo aproximado. Ya que la inflación es muy difícil de medir y en algunos productos impacta más que en otros. Incluso puede ser impredecible. Pero esta sería la teoría en líneas generales. </p>
	<h2> La meta sería que puedas dividir tus gastos aplicando la regla 50/30/20 </h2>
	<IMG class="imagen" SRC="images/rata2.jpg" alt="rata queso" width="250px" height="200px" ALIGN=LEFT>  <br><br>
	<p> El 50% del dinero de tu sueldo lo tienes que dedicar a gastos básicos. Estos se van en la hipoteca o alquiler, servicios como luz, gas, internet, alimentos, etc. Son gastos que tienes que llevar a cabo sí o sí. Por eso abarcan el 50% de tu sueldo, para prevenir. Si te sobra dinero, mucho mejor. No lo gastes en cosas innecesarias y súmalo al porcentaje de ahorro. </p>
	<p> El 30% va destinado a gastos personales. Es decir, todo aquello que puedas hacer durante el mes. Quizá compres algo de ropa, otro mes puedes gastarlo en más actividades de ocio, en viajar o algún otro gusto que desees darte. Recuerda que si te sobra debes sumarlo al porcentaje de ahorro. </p>
	<p> Un 20% del dinero que ganas tiene que ir dedicado al ahorro. Es una excelente práctica la de “pagarte primero”, es decir antes de gastar nada apartar el 20%. De aquí saldrán las compras planificadas que  mencionamos anteriormente, la amortización de tus bienes, y también un fondo de emergencia por cualquier contrariedad. </p> <br>
